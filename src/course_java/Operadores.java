package course_java;

public class Operadores {

	public static void main(String[] args) {

		double n1 = 10;
		double n2 = 30;
		String s = "abc";
		System.out.println(n1);
		System.out.println(n2);
		System.out.println(n1);
		
		n1 *= 2;
		n2 += n1;
		s += "def";
		System.out.println("-------------");
		System.out.println(n1);
		System.out.println(n2);
		System.out.println(s);
		
		
		n1++;
		System.out.println(n1);
		
	}

}
